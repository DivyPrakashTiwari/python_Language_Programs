# This code snippet creates a list of strings containing personal information
# and then iterates through the list to print each element.
l = ["NAME : DIVY", "ROLL : CSF21034", "REGD : 2101020449", "BRANCH: CSF"]

# Initialize a for loop to iterate through the list using the range() function
for i in range(4):
    
    # Print the current element in the list
    print(l[i])
    
    # Increment the value of i by 1
    i = i + 1