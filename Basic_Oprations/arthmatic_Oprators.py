# This script takes two integer inputs, 'a' and 'b', and performs various operations on them.
# It then prints the results of these operations.

a = int(input("Enter the value of a: "))  # Prompting user for the value of 'a' and converting the input to an integer.

b = int(input("Enter the value of b: "))  # Prompting user for the value of 'b' and converting the input to an integer.

s = a + b  # Calculating the sum of 'a' and 'b'.

sub = a - b  # Calculating the difference between 'a' and 'b'.

pro = a * b  # Calculating the product of 'a' and 'b'.

div = a // b  # Calculating the quotient of 'a' divided by 'b'.

print("Sum:", s)  # Printing the sum of 'a' and 'b'.

print("Sub:", sub)  # Printing the difference between 'a' and 'b'.

print("Pro = ", pro)  # Printing the product of 'a' and 'b'.

print(div)  # Printing the quotient of 'a' divided by 'b'.