# This code converts temperature from Celsius to Fahrenheit

c = float(input("Enter the temperature in Celsius: "))  # Prompt user to input temperature in Celsius

# Formula to convert Celsius to Fahrenheit: F = 32 + (9/5) * C
f = 32 + (9/5) * c

# Print the temperature in Fahrenheit
print("The temperature in Fahrenheit is: ", f)